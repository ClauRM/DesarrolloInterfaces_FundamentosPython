dia = "lunes"

if dia == 'lunes':
    print ("Hoy es el peor día de la semana")
elif dia == 'martes':
    print ("Hoy es el segundo día de la semana")
elif dia == 'miercoles':
    print ("Hoy es el tercer día de la semana")
elif dia == 'jueves':
    print ("Hoy es el cuarto día de la semana")
elif dia == 'viernes':
    print ("Hoy es el quinto día de la semana")
elif dia == 'sabado':
    print ("Hoy es el sexto día de la semana")
elif dia == 'domingo':
    print ("Hoy es el septimo día de la semana")
else:
    print ("No corresponde a ningún día de la semana")
    
